    public void TodosClientes(){
    
        List<Utilizador> listaClientes = null;
        //DefaultTableModel tmodel = new DefaultTableModel();
        DefaultTableModel tmodel = (DefaultTableModel)jTable1.getModel();
        tmodel.setRowCount(0);
        listaClientes = UtilizadorJpaController.readAll();

        for(Utilizador uti : listaClientes){
            String [] linhaTabela = new String[6];
            linhaTabela[0] = uti.getUtilizadorId().toString();
            linhaTabela[1] = uti.getUtilizadorNome();
            linhaTabela[2] = uti.getUtilizadorTipo().toString();
            linhaTabela[3] = uti.getUtilizadorMorada();
            linhaTabela[4] = uti.getUtilizadorIdade().toString();
            linhaTabela[5] = uti.getUtilizadorPass();
            
            tmodel.addRow(linhaTabela);
            //System.out.println(linhaTabela[0]);
        }        
        //DefaultTableModel tmodel = new DefaultTableModel();  
        tmodel.fireTableDataChanged();
        
    }
   

