    private void open_site(){
        
       
        BrowserUtil.setVersion(Version.V6_22);
        browser = new Browser();
        browser.addScriptContextListener(new ScriptContextAdapter() {
            @Override
            public void onScriptContextCreated(ScriptContextEvent event) {
                browser.executeJavaScriptAndReturnValue("window").asObject().setProperty("java", new JavaCallback());
            }
        });
        BrowserView view = new BrowserView(browser);

        jPanel1.add(view, BorderLayout.CENTER);
        jPanel1.setSize(900, 500);      
        jPanel1.setVisible(true);

        // Provide the correct full path to the map.html file, e.g. D:\\map.html
        Browser.invokeAndWaitFinishLoadingMainFrame(browser, new Callback<Browser>() {
            @Override
            public void invoke(Browser value) {
                browser.loadURL("C:\\Users\\rafae\\Documents\\NetBeansProjects\\projeto2_v2.1-pontos\\simple_map.html");
            }
        });
        browser.executeJavaScriptAndReturnValue(
           

"    function geocodeLatLng(geocoder, map, infowindow, position) {\n" +
"        const latlng = {\n" +
"          lat: parseFloat(position.lat),\n" +
"          lng: parseFloat(position.lng),\n" +
"        };\n" +
"        geocoder.geocode({ location: latlng }, (results, status) => {\n" +
"          if (status === \"OK\") {\n" +
"            if (results[0]) {\n" +
"              map.setZoom(11);\n" +
"              const marker = new google.maps.Marker({\n" +
"                position: latlng,\n" +
"                map: map,\n" +
"              });\n" +
 "window.java.onMarkerAdded(position, results[0].formatted_address.toString());   "+
        "} "+
        "}});"
        + "}"

                       );
    }

    public class JavaCallback {
        public void onMarkerAdded(JSObject position, String address){
           System.out.println(address);
            //System.out.println(position.getProperty("lat").getNumberValue());
            double lattemp = Double.parseDouble(position.getProperty("lat").toString()); 
            double lngtemp = Double.parseDouble(position.getProperty("lng").toString()); 
          //double lat = position.getProperty("lat").asFunction().invoke(position).asNumber().getDouble();
          //double lng = position.getProperty("lng").asFunction().invoke(position).asNumber().getDouble(); 
           String latitude = Double.toString(lattemp);
           Lat.setText(latitude);
           String longitude = Double.toString(lngtemp);
           Long.setText(longitude);
      
       
        String[] splitted = address.split(",");
        for (int i=0; i<splitted.length; i++){
            morada.setText(splitted[0]);
            Cp.setText(splitted[1]);
            pais.setText(splitted[2]);
        }

           
        }
        
  
    }
    
  


