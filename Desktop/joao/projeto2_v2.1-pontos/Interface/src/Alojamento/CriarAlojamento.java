    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        if (modoOper == 0) {
            if (!Nome.getText().isEmpty() && !Lat.getText().isEmpty() && !Long.getText().isEmpty() && !Cp.getText().isEmpty() && !Descricao.getText().isEmpty() && !pais.getText().isEmpty() ){     
            Alojamento alo = new Alojamento();

            alo.setAlojamentoNome(Nome.getText());
            //String temp = cidade.getSelectedItem().toString();
          //  alo.setAlojamentoCidade(temp);
            alo.setAlojamentoLatitude(Lat.getText());
            alo.setAlojamentoLongitude(Long.getText());
            alo.setAlojamentoCp(Cp.getText());
            alo.setAlojamentoCidade(morada.getText());
            alo.setAlojamentoPais(pais.getText());
            alo.setAlojamentoDescricao(Descricao.getText());
            

            AlojamentoJpaController.create(alo);

            JOptionPane.showMessageDialog(null, "Alojamento criado Com sucesso!");
            ListarAlojamento listarAlojamento = new ListarAlojamento();
            listarAlojamento.setVisible(true);            
            this.setVisible(false);
            }
            else{
                this.jLabel13.setText("Campos Vazios");
            }
             }
        else {
            if(!Nome.getText().isEmpty() && !Lat.getText().isEmpty() && !Long.getText().isEmpty() && !Cp.getText().isEmpty() && !Descricao.getText().isEmpty()){
             AloToUpdate.setAlojamentoNome(Nome.getText());
             //AloToUpdate.setAlojamentoCidade(Cidade.getText());
             AloToUpdate.setAlojamentoLatitude(Lat.getText());
             AloToUpdate.setAlojamentoLongitude(Long.getText());
             AloToUpdate.setAlojamentoCp(Cp.getText());
             AloToUpdate.setAlojamentoPais(pais.getText());
             AloToUpdate.setAlojamentoCidade(morada.getText());
             AloToUpdate.setAlojamentoDescricao(Descricao.getText());
             
            AlojamentoJpaController.update(AloToUpdate);
            JOptionPane.showMessageDialog(null, "Alojamento editado Com sucesso!");
            ListarAlojamento listarAlojamento = new ListarAlojamento();
            listarAlojamento.setVisible(true);            
            this.setVisible(false);
            }
            else{
                this.jLabel13.setText("Campos Vazios2");
            }
        }
// TODO add your handling code here:
    }//GEN-LAST:event_addActionPerformed

