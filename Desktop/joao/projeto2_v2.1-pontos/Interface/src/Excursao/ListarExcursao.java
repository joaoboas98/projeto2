    public void TodosExcursao(){
    
        List<Excursao> listaGrupos = null;
        //DefaultTableModel tmodel = new DefaultTableModel();
        DefaultTableModel tmodel = (DefaultTableModel)jTable1.getModel();
        tmodel.setRowCount(0);
        listaGrupos= ExcursaoJpaController.readAll();

        for(Excursao gru : listaGrupos){
            String [] linhaTabela = new String[6];
            linhaTabela[0] = gru.getExcursaoId().toString();
            linhaTabela[1] = gru.getGrupoId().getGrupoNome();
            linhaTabela[2] = gru.getUtilizadorId().getUtilizadorNome();
            linhaTabela[3] = gru.getGuiaId().getGuiaNome();
            linhaTabela[4] = gru.getTrilhoId().getTrilhoNome();
//            linhaTabela[5] = gru.getDataInicio().toString();
  //          linhaTabela[6] = gru.getDataFim().toString();
            tmodel.addRow(linhaTabela);
            //System.out.println(linhaTabela[0]);
        }        
        //DefaultTableModel tmodel = new DefaultTableModel();  
        tmodel.fireTableDataChanged();
        
    }

